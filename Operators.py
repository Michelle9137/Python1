#Arithmetic operators
x = 10
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - returns the remainder

#Comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) # Not equal to


#Assignment operators
w = 7
y = 12

print(w)

w += 2

print(w)

y -= 4
print(y)

y /= 4
print(y)

#Logical operators -And, Not, Or
print(34 < 4 and 56 > 21)
print(not(34 < 4 or 56 > 21))
print(34 < 4 or 56 > 21)
